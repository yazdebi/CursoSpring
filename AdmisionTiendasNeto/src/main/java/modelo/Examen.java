package modelo;

public class Examen {
	int IdExamen;
	int IdVisitante;
	String [] aptitutes;
	public int getIdExamen() {
		return IdExamen;
	}
	public void setIdExamen(int idExamen) {
		IdExamen = idExamen;
	}
	public int getIdVisitante() {
		return IdVisitante;
	}
	public void setIdVisitante(int idVisitante) {
		IdVisitante = idVisitante;
	}
	public String[] getAptitutes() {
		return aptitutes;
	}
	public void setAptitutes(String[] aptitutes) {
		this.aptitutes = aptitutes;
	}
	
	
	

}
